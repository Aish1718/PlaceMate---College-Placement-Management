function AppRoutes() {
  const { user, loading } = useAuth();

  if (loading) {
    return <div>Loading...</div>;
  }

  return (
    <Routes>
      <Route path="/" element={<LandingPage />} />
      <Route path="/login" element={!user ? <Login /> : <Navigate to="/dashboard" />} />
      <Route path="/register" element={!user ? <Register /> : <Navigate to="/dashboard" />} />
      <Route
        path="/dashboard"
        element={
          <PrivateRoute>
            {user?.is_superuser && <CoordinatorDashboard />}
            {!user?.is_superuser && user?.role === 'student' && <StudentDashboard />}
            {!user?.is_superuser && user?.role === 'placement_coordinator' && <CoordinatorDashboard />}
            {!user?.is_superuser && user?.role === 'college_management' && <ManagementDashboard />}
            {!user?.is_superuser && user?.role === 'company' && <CompanyDashboard />}
            {!user && <Navigate to="/login" />}
          </PrivateRoute>
        }
      />
    </Routes>
  );
}

function App() {
  return (
    <ThemeProvider theme={theme}>
      <CssBaseline />
      <AuthProvider>
        <Router>
          <AppRoutes />
        </Router>
      </AuthProvider>
    </ThemeProvider>
  );
}

